#include "WanderBehaviour.h"

///////////////////////////////////////////////////////////////////////////////////////////////////
void WanderBehaviour::update(Plane * t_self, float t_delta)
{
    if (rand() % 4 == 0)
    {
        if (rand() % 2 == 0)
            t_self->rotateLeft(t_delta);
        else
            t_self->rotateRight(t_delta);
    }
}

