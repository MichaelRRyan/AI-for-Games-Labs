#include "NPC.h"

///////////////////////////////////////////////////////////////////////////////////////////////////
NPC::NPC() :
	Plane(50.0f, 0.5f, 250.0f, 45.0f)
{
	setPlaneType(2);
	m_behaviour = new WanderBehaviour();
}

///////////////////////////////////////////////////////////////////////////////////////////////////
NPC::~NPC()
{
	if (m_behaviour) delete m_behaviour;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
void NPC::update(float t_delta)
{
	SteeringInput input;
	input.me = this;
	m_behaviour->getSteering(input, t_delta);

	Plane::update(t_delta);
}

