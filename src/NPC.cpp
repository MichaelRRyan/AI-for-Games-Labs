#include "NPC.h"

///////////////////////////////////////////////////////////////////////////////////////////////////
NPC::NPC(Behaviour * t_behaviour) :
	Plane(50.0f, 0.5f, 250.0f, 45.0f)
{
	setPlaneType(2);
	m_behaviour = t_behaviour;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
NPC::~NPC()
{
	if (m_behaviour) delete m_behaviour;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
void NPC::update(float t_delta)
{
	m_behaviour->update(this, t_delta);
	Plane::update(t_delta);
}

